public class Main {
    public static void main(String[] args) {
        int num1 = 10; // Перше число
        int num2 = 3;  // Друге число

        int quotient = num1 / num2;  // Результат цілочисельного ділення
        int remainder = num1 % num2; // Залишок від ділення

        System.out.println("Результат ділення: " + quotient);
        System.out.println("Залишок від ділення: " + remainder);
    }
}